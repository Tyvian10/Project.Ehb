public class Vliegtuig {


    private String flightcheck;
    private int plaatsenEconomy;
    private int plaatsenBussiness;

    public Vliegtuig(int plaatsenEconomy, int plaatsenBussiness) {


        this.flightcheck = flightcheck;

        this.plaatsenEconomy = plaatsenEconomy;
        this.plaatsenBussiness = plaatsenBussiness;


    }

    public String getFlightcheck() {
        return flightcheck;
    }

    public void setFlightcheck(String flightcheck) {
        this.flightcheck = flightcheck;
    }

    public int getPlaatsenEconomy() {
        return plaatsenEconomy;
    }

    public void setPlaatsenEconomy(int plaatsenEconomy) {
        this.plaatsenEconomy = plaatsenEconomy;
    }

    public int getPlaatsenBussiness() {
        return plaatsenBussiness;
    }

    public void setPlaatsenBussiness(int plaatsenBussiness) {
        this.plaatsenBussiness = plaatsenBussiness;
    }

}