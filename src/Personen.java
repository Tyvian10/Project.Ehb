public class Personen {
    private String name;
    private int age;
    private String address;

    public Personen(String name, int age, String address) {
        this.name = name;
        this.age = age;
        this.address = address;
    }

    // Getters en setters
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getAddress() {
        return address;
    }
}


